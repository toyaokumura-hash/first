# 九九表を出力してください。具体的には、出力のi行j列目 (1 ≦ i, j ≦ 9) の数値は i * j になるように出力してください。
# coding: utf-8
# 自分の得意な言語で
# Let's チャレンジ！！
for i in range(1,10): #横行を9までで設定する
    N = [i * j for j in range(1, 10)] #横*縦のループを作りつつ、縦も9までで設定する。これで九九も一の段になる
    print(*N) #一の段が終わったら改行されるようにして九の段まで作られる
