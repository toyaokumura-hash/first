# 文字列の長さを 3 で割ったあまりを M とします。入力された文字列の最初の M 文字を除くと、残りの文字列の長さは 3 の倍数となり、
# 「特定の区切り文字を使って出力 6 : 大きな数値を 3 けたごとにカンマ区切りで出力」と同じ問題として解けます。
N = input() #文字列で入力
x = len(N) % 3 #文字数を3で割ったあまり

for i in range(len(N)):
    if (i - x) % 3 == 0 and i != 0:
        print(",", end="")

    print(N[i], end="")

print()

