# 9 個の数値が半角スペース区切りで入力されます。この数値を 3 行 3 列の形式で出力してください。
# 具体的には、入力された数値を N_1 , N_2 , N_3 , ..., N_9 とするとき、
# 1 行目にはN_1 からN_3 を、2 行目には N_4 から N_6 を、3 行目には N_7 から N_9 を出力してください。
# まず、9 個の数値をすべて受け取ります。
# 次に、3 つずつ数値を出力するのですが、ループカウンタの値によって半角スペースを出力するか、改行を出力するか切り替えます。
# coding: utf-8
# 自分の得意な言語で
# Let's チャレンジ！！
N = input().split() #数値を文字列で受け取ってリストで返し

for i in range(len(N)):
    print(N[i], end="") #改行なしで出力
    if i % 3 == 2: #3回に1回
        print() #改行する
    else: #そうじゃない時
        print(" ", end="") #改行なしで半角スペース入れる

