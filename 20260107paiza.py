# リストの要素 (N[i]) は毎回同様の出力をするので簡略して書き、出力する文字列の様子を{出力する値}のように書きます。
# i=0 : 半角スペースを改行無しで出力 {N[0] }
# i=1 : 半角スペースを改行無しで出力 (N[0] N[1] )
# i=2 : 改行する {N[0] N[1] N[2] (改行)}
